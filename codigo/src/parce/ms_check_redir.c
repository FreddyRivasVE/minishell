#include "minishell.h"

bool    ms_check_redir(char *str)
{
    int i;

    i = 0;
    while (str[i] && str[i] == ' ')
        i++;
    if (str[i] == '<')
    {
        i++;
        while (str[i])
        {
            if (str[i] == ' ')
                i++;
        }
        
    }
    
}